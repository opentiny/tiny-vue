<template>
  <div
    class="tiny-mobile-progress"
    :class="[
      'tiny-mobile-progress--' + type,
      status ? 'is-' + status : '',
      {
        'tiny-mobile-progress__without-text': !showText,
        'tiny-mobile-progress__inside-text': textInside
      }
    ]"
    role="progressbar"
    :aria-valuenow="percentage"
    aria-valuemin="0"
    aria-valuemax="100"
  >
    <div class="tiny-mobile-progress-bar" v-if="type === 'line'">
      <div class="tiny-mobile-progress-bar__outer" :style="{ height: strokeWidth + 'px' }">
        <div class="tiny-mobile-progress-bar__inner" :style="state.barStyle">
          <div class="tiny-mobile-progress-bar__innerText" v-if="showText && textInside && strokeWidth > 12">
            {{ state.content }}
          </div>
        </div>
      </div>
    </div>
    <div class="tiny-mobile-progress-circle" :style="{ height: width + 'px', width: width + 'px' }" v-else>
      <svg viewBox="0 0 100 100">
        <path
          class="tiny-mobile-progress-circle__track"
          :d="state.trackPath"
          stroke="#e5e9f2"
          :stroke-width="state.relativeStrokeWidth"
          fill="none"
          :style="state.trailPathStyle"
        ></path>
        <path
          class="tiny-mobile-progress-circle__path"
          :d="state.trackPath"
          :stroke="state.stroke"
          fill="none"
          stroke-linecap="round"
          :stroke-width="percentage ? state.relativeStrokeWidth : 0"
          :style="state.circlePathStyle"
        ></path>
      </svg>
    </div>
    <div
      class="tiny-mobile-progress-text"
      v-if="showText && !textInside"
      :style="{ fontSize: state.progressTextSize + 'px' }"
    >
      <template v-if="!status">{{ state.content }}</template>
      <component v-else :is="state.iconClass" />
    </div>
  </div>
</template>

<script lang="ts">
import { renderless, api } from '@opentiny/vue-renderless/progress/vue'
import { props, setup, defineComponent } from '@opentiny/vue-common'
import { iconClose, iconSuccessful, iconError, iconYes, iconWarning } from '@opentiny/vue-icon'
import '@opentiny/vue-theme-mobile/progress/index.less'
import type { IProgressApi } from '@opentiny/vue-renderless/types/progress.type'

export default defineComponent({
  props: [
    ...props,
    'type',
    'percentage',
    'status',
    'strokeWidth',
    'textInside',
    'width',
    'showText',
    'color',
    'format'
  ],
  components: {
    IconClose: iconClose(),
    IconSuccessful: iconSuccessful(),
    IconError: iconError(),
    IconYes: iconYes(),
    IconWarning: iconWarning()
  },
  setup(props, context) {
    return setup({ props, context, renderless, api }) as unknown as IProgressApi
  }
})
</script>
